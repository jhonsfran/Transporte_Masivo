    private void btRPqrsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRPqrsActionPerformed
        // TODO add your handling code here:
        Conexion conect = new Conexion();
        conect.conectar();
        String path = null;
        String ts = new SimpleDateFormat("dd-MM-yyyy-HH.mm.ss").format(new java.util.Date());
        
        java.util.Date fecha1, fecha2;
        fecha1 = jxDkFechaDesde.getDate();
        fecha2 = jxDkFechaHasta.getDate();
        
        HashMap parametros = new HashMap<String, Object>();
        
        parametros.put("fecha1", fecha1);
        parametros.put("fecha2", fecha2);
        
        int index = cbEstacion.getSelectedIndex();
        String estacion = Integer.toString(index);
        String estacion_sql;
        
        if(index != 0){
            
            estacion_sql = "AND estacion pqrs_estacion = '"+ estacion +"'";
        }else{
            estacion = "";
            estacion_sql = "";
        }
        
        
        parametros.put("estacion_sql", estacion_sql);
        parametros.put("estacion", estacion);
        
        try {
            path = new java.io.File(".").getCanonicalPath();
        } catch (IOException ex) {
            System.out.println(ex);
        }

        try {

            String reporte = path + "\\src\\Reportes\\PQRS.jasper";
            String pdfName = path + "\\Reportes\\PQRS"+ ts +".pdf";
            JasperPrint print = JasperFillManager.fillReport(reporte, parametros, conect.getConexion());

            JasperViewer jasperViewer = new JasperViewer(print);
            jasperViewer.setVisible(true);
            JasperExportManager.exportReportToPdfFile(print, pdfName);

            conect.cerrar();

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btRPqrsActionPerformed

