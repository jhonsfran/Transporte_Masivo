    private void asignaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asignaActionPerformed
        // TODO add your handling code here:

        DAOEmpleadoImpl mi_empleado = new DAOEmpleadoImpl();

        try {

            if (!(turno_id.getText()).matches("[0-9]*")) {
                JOptionPane.showMessageDialog(null, "El id del turno debe ser un número");
            }else if (turno_id.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe digitar el id del turno");
            } else if (empleado_id.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe digitar el id del empleado");
            } else {
                String mensaje = mi_empleado.asignarTurno(empleado_id.getText(), Integer.parseInt(turno_id.getText()));
                JOptionPane.showMessageDialog(null, mensaje);

                turno_id.setText("");
                empleado_id.setText("");

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_asignaActionPerformed

