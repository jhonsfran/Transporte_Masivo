    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        DAObusImpl mi_bus = new DAObusImpl();
        String placa_bus = bus_placa.getText();

        if (placa_bus.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor dígite la placa del bus");
        } else {
            try {
                if (!mi_bus.existe(placa_bus)) {
                    JOptionPane.showMessageDialog(null, "El bus que busca no existe");
                } else {

                    guardar.setEnabled(false);
                    eliminar.setEnabled(false);

                    this.setVisible(false);

                    JButton actualizar = new JButton("Actualizar");//creo un boton
                    //actualizar.setBounds(new Rectangle(50, 50, 100, 75));
                    //actualizar.setSize(80, 29);
                    actualizar.reshape(170, 10, 100, 27);// x,y,largo,ancho
                    this.jPanel1.add(actualizar);
                    bus_placa.setEnabled(false);
                    this.setVisible(true);

                    actualizar.addActionListener(new java.awt.event.ActionListener() {
                        @Override
                        public void actionPerformed(java.awt.event.ActionEvent evt) {

                            try {

                                //JOptionPane.showMessageDialog(null, estacion_ubicacion);

                                if (validaCampos()) {

                                    int id_tipo_bus = 0;

                                    switch(tipo_bus.getSelectedItem().toString()){
                                        case "ALIMENTADOR":
                                            id_tipo_bus = 1;
                                            break;

                                        case "PADRON":
                                            id_tipo_bus = 2;
                                            break;

                                        case "ARTICULADO":
                                            id_tipo_bus = 3;
                                            break;

                                        default:
                                            id_tipo_bus = 0;
                                            break;
                                    }


                                    String estado = mi_bus.modificar(placa_bus,placa_bus,id_tipo_bus,ruta_bus.getSelectedItem().toString());

                                    JOptionPane.showMessageDialog(null, estado);

                                    if (estado.equalsIgnoreCase("actualización exitosa")) {
                                        //System.exit(0);
                                        jPanel1.remove(actualizar);
                                        setVisible(false);
                                        bus_placa.setText("");

                                        guardar.setEnabled(true);
                                        eliminar.setEnabled(true);
                                        bus_placa.setEnabled(true);

                                        setVisible(true);

                                    }
                                }

                            } catch (Exception ex) {
                                //Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
                                JOptionPane.showMessageDialog(null, ex);
                            }

                        }
                    });
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
                Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarActionPerformed
        // TODO add your handling code here:

        Conexion conect = new Conexion();
        conect.conectar();
        String path = null;

        try {
            path = new java.io.File(".").getCanonicalPath();
        } catch (IOException ex) {
            Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {

            //System.out.println(path + "\\src\\Vista\\Empleados.jasper");
            JasperReport reporte = JasperCompileManager.compileReport(path + "\\src\\Reportes\\Buses.jrxml");
            JasperPrint print = JasperFillManager.fillReport(reporte, null, conect.getConexion());

            JasperViewer jasperViewer = new JasperViewer(print);
            jasperViewer.setVisible(true);

            conect.cerrar();

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_ListarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:

        DAObusImpl mi_bus = new DAObusImpl();
        String id_bus = bus_placa.getText();

        if (id_bus.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor digite la placa del bus que desea eliminar");
        } else {

            try {

                String estado = mi_bus.eliminar(id_bus);
                JOptionPane.showMessageDialog(null, estado);

                if (estado.equalsIgnoreCase("eliminación exitosa")) {
                    bus_placa.setText("");
                }

            } catch (Exception ex) {
                //Logger.getLogger(CrudUsuario.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex);
            }

        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:

        DAObusImpl mi_bus = new DAObusImpl();

        if (validaCampos()) {
            
            int id_tipo_bus = 0;
            
            switch(tipo_bus.getSelectedItem().toString()){
                case "ALIMENTADOR":
                    id_tipo_bus = 1;
                    break;
                   
                case "PADRON":
                    id_tipo_bus = 2;
                    break;
                    
                case "ARTICULADO":
                    id_tipo_bus = 3;
                    break;
                    
                default:
                    id_tipo_bus = 0;
                    break;
            }

            try {
                String estado_insersion = mi_bus.registrar(bus_placa.getText(),id_tipo_bus,ruta_bus.getSelectedItem().toString());
                JOptionPane.showMessageDialog(null, estado_insersion);

                if (!estado_insersion.equalsIgnoreCase("Insersión exitosa")) {
                    bus_placa.setText("");
                }

            } catch (Exception ex) {
                //Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex);
            }

        }
    }//GEN-LAST:event_guardarActionPerformed

