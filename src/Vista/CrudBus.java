    public CrudBus() {
        initComponents();
        
        DAORutaImpl mi_ruta = new DAORutaImpl();
        List<String> rutas = new ArrayList<String>();
        
        try {
            rutas = mi_ruta.listar();
            
            if (rutas.isEmpty()) {
                rutas.add("No existe Administrador en la BD");//seteo la posicion cero por si no hay datos
                guardar.setEnabled(false);
            }
            
            Iterator iter = rutas.iterator();
            while (iter.hasNext()) {
                
                if(!iter.next().toString().equals("")){
                    this.ruta_bus.addItem(iter.next().toString());
                }
            }
            
        } catch (Exception ex) {
            //Logger.getLogger(CrudEstacion.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
