    public boolean validaCampos() {

        boolean valida = false;

        if (usuario_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor dígite la identificación del usuario");
        } else if (usuario_nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor dígite el nombre del usuario");
        } else if (usuario_telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor dígite el telefono del usuario");
        } else {
            valida = true;
        }

        return valida;
    }

