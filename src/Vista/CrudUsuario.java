    private void consultarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarUsuariosActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        DAOUsuarioImpl usuario = new DAOUsuarioImpl();
        DAOTarjetaImpl tarjeta = new DAOTarjetaImpl();

        if (tarjeta_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor dígite el id de la tarjeta");
        }else {
            try {
                if(!tarjeta.existe(Integer.parseInt(tarjeta_id.getText()))){
                    JOptionPane.showMessageDialog(null, "La tarjeta que busca no existe");
                }else{

                    CrearUsuario.setEnabled(false);
                    eliminarUsuario.setEnabled(false);
                    
                    String usuario_a_ingresar = "";
                    Tarjeta mi_tarjeta = tarjeta.buscar(Integer.parseInt(tarjeta_id.getText()));
                    
                    if(mi_tarjeta.getUsuario().getUsuarioId().equals("0")){//si es usuario genérico, pide un usuario a actualizar
                        usuario_a_ingresar = JOptionPane.showInputDialog("Introduzca el usuario que desea personalizar: ");
                    }else{//si la tarjeta ya tiene un usuario, trae los datos de ese usuario
                        usuario_a_ingresar = mi_tarjeta.getUsuario().getUsuarioId();
                    }
                    
                  
                    Usuario miusuario = usuario.buscar(usuario_a_ingresar);
                    
                    if(usuario.existe(usuario_a_ingresar)){
                        
                        usuario_id.setText(miusuario.getUsuarioId());
                        usuario_nombre.setText(miusuario.getUsuarioNombre());
                        usuario_telefono.setText(miusuario.getUsuarioTelefono());
                        tarjeta_id.setText("" + mi_tarjeta.getTarjetaPin() + "");
                        tarjeta_saldo.setText(mi_tarjeta.getTarjetaSaldo());

                        String estado = "";

                        if (mi_tarjeta.isTarjetaEstado()) {
                            estado = "ACTIVO";
                        } else {
                            estado = "INACTIVO";
                        }

                        tarjeta_estado.setSelectedItem(estado);
                        tarjeta_saldo.setEnabled(false);//el saldo no lo puede tocar por este módulo
                        tarjeta_id.setEnabled(false);

                        this.setVisible(false);

                        JButton actualizar = new JButton("Actualizar");//creo un boton
                        actualizar.setSize(80, 29);
                        actualizar.reshape(110, 10, 100, 27);// x,y,largo,ancho
                        this.CrudUsuario.add(actualizar);
                        this.setVisible(true);

                        actualizar.addActionListener(new java.awt.event.ActionListener() {
                            @Override
                            public void actionPerformed(java.awt.event.ActionEvent evt) {

                                String id_usuario_old = miusuario.getUsuarioId();

                                try {

                                    if (validaCampos()) {

                                        boolean estado_tarjeta = false;
                                        if (tarjeta_estado.getSelectedItem().toString().equalsIgnoreCase("ACTIVO")) {
                                            estado_tarjeta = true;
                                        }

                                        String estado_update_usuario = usuario.modificar(id_usuario_old, usuario_id.getText(), usuario_nombre.getText(), usuario_telefono.getText());
                                        String estado_update_tarjeta = tarjeta.modificar(Integer.parseInt(tarjeta_id.getText()), usuario_id.getText(), id_usuario_old, tarjeta_saldo.getText(), estado_tarjeta);

                                        
                                        JOptionPane.showMessageDialog(null, estado_update_usuario + " de usuario");
                                        JOptionPane.showMessageDialog(null, estado_update_tarjeta + " de tarjeta");

                                        if (estado_update_tarjeta.equalsIgnoreCase("actualización exitosa") && estado_update_usuario.equalsIgnoreCase("actualización exitosa")) {

                                            //System.exit(0);
                                            CrudUsuario.remove(actualizar);
                                            setVisible(false);
                                            setCampos();

                                            CrearUsuario.setEnabled(true);
                                            eliminarUsuario.setEnabled(true);
                                            tarjeta_saldo.setEnabled(true);//el saldo no lo puede tocar por este módulo
                                            tarjeta_id.setEnabled(true);

                                            setVisible(true);

                                        }
                                    }

                                } catch (Exception ex) {
                                    Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
                                }

                            }
                        });
                    
                    }else{
                        JOptionPane.showMessageDialog(null, "El usuario no existe, debe crearlo primero.");
                        setCampos();
                        CrearUsuario.setEnabled(true);
                        eliminarUsuario.setEnabled(true);
                        tarjeta_saldo.setEnabled(true);//el saldo no lo puede tocar por este módulo
                        tarjeta_id.setEnabled(true);
                        
                        tarjeta_estado.setEnabled(false);
                        tarjeta_saldo.setEnabled(false);//el saldo no lo puede tocar por este módulo
                        tarjeta_id.setEnabled(false);
                        
                    }

                    
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_consultarUsuariosActionPerformed

    private void ListarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarUsuariosActionPerformed
        // TODO add your handling code here:

        Conexion conect = new Conexion();
        conect.conectar();
        String path = null;

        try {
            path = new java.io.File(".").getCanonicalPath();
        } catch (IOException ex) {
            Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {

            //System.out.println(path + "\\src\\Vista\\Empleados.jasper");

            JasperReport reporte = JasperCompileManager.compileReport(path + "\\src\\Reportes\\Usuarios.jrxml");
            JasperPrint print = JasperFillManager.fillReport(reporte, null, conect.getConexion());

            JasperViewer jasperViewer = new JasperViewer(print);
            jasperViewer.setVisible(true);

            conect.cerrar();

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_ListarUsuariosActionPerformed

