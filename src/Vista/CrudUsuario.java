    private void eliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarUsuarioActionPerformed
        // TODO add your handling code here:
        DAOUsuarioImpl mi_usuario = new DAOUsuarioImpl();
        DAOTarjetaImpl tarjeta = new DAOTarjetaImpl();

        if (tarjeta_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor digite el id de la tarjeta");
        }else{
            
            try {
                if (!tarjeta.existe(Integer.parseInt(tarjeta_id.getText()))) {
                    JOptionPane.showMessageDialog(null, "La tarjeta que busca no existe");
                }else{

                    Tarjeta mi_tarjeta = tarjeta.buscar(Integer.parseInt(tarjeta_id.getText()));
                    String estado_tarjeta = tarjeta.eliminar(mi_tarjeta.getTarjetaPin());

                    JOptionPane.showMessageDialog(null, estado_tarjeta);

                    if (estado_tarjeta.equalsIgnoreCase("eliminación exitosa")) {

                        String estado = mi_usuario.eliminar(mi_tarjeta.getUsuario().getUsuarioId());
                        JOptionPane.showMessageDialog(null, estado);

                        if(estado.equalsIgnoreCase("eliminación exitosa")){

                            setVisible(false);
                            setCampos();
                            setVisible(true);

                        }

                    }

                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

        }
    }//GEN-LAST:event_eliminarUsuarioActionPerformed

    private void CrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearUsuarioActionPerformed
        // TODO add your handling code here:
        DAOUsuarioImpl mi_usuario = new DAOUsuarioImpl();
        
        if (validaCampos()) {
            try {

                String estado = mi_usuario.registrar(usuario_id.getText(), usuario_nombre.getText(),usuario_telefono.getText());
                JOptionPane.showMessageDialog(null, estado);

                if (estado.equalsIgnoreCase("Insersion exitosa")) {
                    setVisible(false);
                    setCampos();
                    setVisible(true);
                    
                    tarjeta_estado.setEnabled(true);
                    tarjeta_saldo.setEnabled(true);//el saldo no lo puede tocar por este módulo
                    tarjeta_id.setEnabled(true);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

        }

    }//GEN-LAST:event_CrearUsuarioActionPerformed

