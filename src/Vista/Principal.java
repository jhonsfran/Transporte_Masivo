    private void btRecargarTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRecargarTActionPerformed
        // TODO add your handling code here:
        
    String id_tarjeta = "";
    String saldo = "";

    DAOTarjetaImpl tarjeta = new DAOTarjetaImpl();
    
    do{
        id_tarjeta = JOptionPane.showInputDialog("Introduce el id de la tarjeta a recargar: ");
        saldo = JOptionPane.showInputDialog("Introduce la cantidad de dinero a recargar: ");
    
    }while(id_tarjeta.equals("") && saldo.equals(""));

        try {
            String mensaje_recarga = tarjeta.recarga(Integer.parseInt(id_tarjeta), saldo, this.getUserLogeado());
            
            //OptionPane.showMessageDialog(null, mensaje_recarga);
            
            if (mensaje_recarga.equalsIgnoreCase("Recarga exitosa")) {
                
                JOptionPane.showMessageDialog(null, mensaje_recarga);
                
                id_tarjeta = "";
                saldo = "";
            }else{
                JOptionPane.showMessageDialog(null, mensaje_recarga);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            //Logger.getLogger(CrudEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }//GEN-LAST:event_btRecargarTActionPerformed

