    private void btAbordarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAbordarActionPerformed
        // TODO add your handling code here:
        
        String id_tarjeta = "";
        String id_ruta = "";

        DAOTarjetaImpl tarjeta = new DAOTarjetaImpl();

        do {
            id_tarjeta = JOptionPane.showInputDialog("Introduce el id de la tarjeta: ");
            id_ruta = JOptionPane.showInputDialog("Introduce el id de la ruta a la cual ingresas: ");

        } while (id_tarjeta.equals(""));
        
        try {
            String mensaje = tarjeta.descuenta(Integer.parseInt(id_tarjeta),id_ruta);
            JOptionPane.showMessageDialog(null, mensaje);
            id_tarjeta = "";

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btAbordarActionPerformed

    private void btConsultarTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarTurnoActionPerformed
        // TODO add your handling code here:
        DAOEmpleadoImpl empleado = new DAOEmpleadoImpl();
        String id_empleado = "";

        do {
            id_empleado = JOptionPane.showInputDialog("Introduce el id del conductor: ");

        } while (id_empleado.equals(""));

        try {
            String mensaje = empleado.consultarTurno(id_empleado);

            if (!mensaje.equalsIgnoreCase("El empleado no tiene turno asignado")) {

                JOptionPane.showMessageDialog(null, mensaje);
                id_empleado = "";

            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btConsultarTurnoActionPerformed

