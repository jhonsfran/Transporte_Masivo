    public void moduloApp(int rol){
    
        switch (rol) {

            case 1://conductor

                usuario.setVisible(false);
                auxiliar.setVisible(false);
                gerente.setVisible(false);
                directorOp.setVisible(false);
                administradorE.setVisible(false);
                
                break;
            case 2://auxiliar

                usuario.setVisible(false);
                gerente.setVisible(false);
                conductor.setVisible(false);
                directorOp.setVisible(false);
                administradorE.setVisible(false);
                
                break;
            case 3://administrador
                
                usuario.setVisible(false);
                auxiliar.setVisible(false);
                gerente.setVisible(false);
                conductor.setVisible(false);
                directorOp.setVisible(false);
                break;
            case 4://director operativo
                usuario.setVisible(false);
                auxiliar.setVisible(false);
                gerente.setVisible(false);
                conductor.setVisible(false);
                administradorE.setVisible(false);
                break;
            case 5://gerente

                break;
            default:
                auxiliar.setVisible(false);
                gerente.setVisible(false);
                conductor.setVisible(false);
                directorOp.setVisible(false);
                administradorE.setVisible(false);
                break;
        }
    }

