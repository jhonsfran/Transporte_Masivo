    private void btConsultarPqrsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarPqrsActionPerformed
        // TODO add your handling code here:
        if(usuario_logeado.equals("0")){
            
            taResultados.setText("Solo los usuarios con tarjeta personalizada pueden utilizar PQRS");
        }
        else {
            DAOUsuarioImpl usr = new DAOUsuarioImpl();
            try {
                int ticket = Integer.parseInt(tfTicket.getText());
                
                if(usr.existePqrs(ticket)){
                    taResultados.setText(usr.consultarPqrs(ticket));
                }
                else {
                    JOptionPane.showMessageDialog(this, "El ticket no existe", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_btConsultarPqrsActionPerformed

